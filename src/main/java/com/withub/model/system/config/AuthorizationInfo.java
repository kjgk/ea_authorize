package com.withub.model.system.config;


public class AuthorizationInfo {

    public String systemUniqueCode;

    public String systemAuthorizationCode;

    public String systemAuthorizationTime;

    public String getSystemUniqueCode() {

        return systemUniqueCode;
    }

    public void setSystemUniqueCode(String systemUniqueCode) {

        this.systemUniqueCode = systemUniqueCode;
    }

    public String getSystemAuthorizationCode() {

        return systemAuthorizationCode;
    }

    public void setSystemAuthorizationCode(String systemAuthorizationCode) {

        this.systemAuthorizationCode = systemAuthorizationCode;
    }

    public String getSystemAuthorizationTime() {

        return systemAuthorizationTime;
    }

    public void setSystemAuthorizationTime(String systemAuthorizationTime) {

        this.systemAuthorizationTime = systemAuthorizationTime;
    }
}
